<p class="first">Real-time Weather Alerts</p>
<p class="second">Real-time weather alerts sent to your smartphone or computer.</p>
<p class="third">Vantage Connect transmits data from your Vantage Pro2 or Vantage Vue Integrated Sensor Suite (ISS) over a cellular connection to the internet in 5, 15, or 60-minute intervals depending your chosen service plan. Regardless of the service plan you choose, Vantage Connect can be programmed to send you vital alarms via email or text in real time when your preset weather conditions, such as high wind or freezing conditions, occur.</p></td>
