<td width="77%" height="173" align="left" valign="top"><p class="first">Remote Weather</p>
<p class="second">What is Vantage Connect?</p>
<p class="third">Vantage Connect combines the data collection capabilities of a weather station console with a data logger and cellular modem to transmit your weather data to a secure page on WeatherLink.com in 5, 15, or 60-minute intervals depending your chosen service plan. <a href="images/apexs_VantageConnect_8Stations_diagram-apexs.jpg" target="_blank">Design your remote weather station</a> to include a Vantage Connect and the Davis sensors that monitor the conditions most important to you. View your data online, anywhere there's an internet connection, using your PC, tablet or smartphone. Or download the data to your PC for analysis, archiving and reporting using the included WeatherLink software.</p></td>
