<p class="first">Davis Weather System Integration</p>
<p class="second">Vantage Connect easily integrates with wireless and cabled systems.</p>
<p class="third">Vantage Connect can take the place of your console or work with your console.<br /><br />
  <strong>Wireless</strong> Vantage Connect works with a Vantage Vue ISS, any wireless Vantage Pro2 ISS or any wireless special-purpose station. The unit can listen to a total of <a href="images/apexs_VantageConnect_8Stations_diagram-apexs.jpg" target="_blank">8 stations in specific combinations</a> and has the same 1000 ft. (300 m) wireless range as our Vantage Vue and Vantage Pro2 consoles and wireless Weather Envoy.<br /><br />
The<strong> cabled</strong> Vantage Connect works only with the cabled Vantage Pro2 Integrated Sensor Suite (ISS) using the 100 ft. (30 m) cable—extends to 1,000 ft. (304,8 m)—from the ISS.</p></td>
