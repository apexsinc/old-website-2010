                    <p>Michael Padua, up on the roof with his   newest weather station, a <a href="http://www.apexvalue.com/weather_station/new_weather/06250-vantage-vue.php" target="_self">Vantage   Vue</a>. It joins Michael's trusty sidekick, <a href="contact-us.php" target="_self">Vantage Pro2</a>,<br />
                       in watching out for storms in Naga   City, Philippines.</p>
                    <table width="683" height="67" border="0" cellpadding="0" cellspacing="0">
                      <tr>
                        <td><p class="style105 style106">David Michael V. Padua is the Executive Director of the Typhoon   Preparedness Center in Naga City, Philippines, and clearly knows his   weather  (and weather stations!). He was recently featured in the <a href="http://opinion.inquirer.net/inquireropinion/talkofthetown/view/20091219-242994/Michael-Padua-One-man-weather-center" target="_blank"><em>The Philippine Daily Inquirer</em></a> as the &quot;One   Man Weather Center.&quot; <br />
                        </p>
                          <p class="style107">The &quot;maverick meteorologist&quot; has been a weather buff since childhood   and may be one of the few who considers himself lucky to live in a place   so well &quot;blessed&quot; with cyclones. He feels that people in the   Philippines have had a &quot; fatalistic<em> bahala na</em> [come-what-may]   attitude&quot; and his mission is to change that.</p>
                          <p class="style107">His web site, <a href="http://www.typhoon2000.ph" target="_blank">Typhoon2000, </a> is a resource to many in Naga City, the Philippines, and   internationally. He tries to make information understandable so   residents can prepare for storms. He even offers a text message updater   for up-to-the-minute storm information. He also registered <a href="http://www.maybagyo.com" target="_blank">maybagyo.com</a>, which also leads<br />
                            to the typhoon2000 website, as a backup system. &ldquo;At least the site is accessible through either of the website addresses even if traffic is high,&rdquo; he said.</p>
                          <p class="style107">When there is a storm, Padua has an adrenaline rush. He find a thrill in the suspense of tracking a storm. His friends tell me that if he is sick, it seems that he get his vitamins from tracking storms. With his laptop and mobile Internet connection, Padua manages to update his website every hour when an area is threatened,<br />
                            compared with the fourtimes- a-day updates from the website of the Philippine Atmospheric, Geophysical and Astronomical Services Administration (Pagasa).</p>
                          <p class="style105"><span class="style106">He has rustled a few feathers at PAGASA, with whom he swears he doesn't   want to compete.  But compete he does. Rather famously, back in 2004, he   forecast that Cyclone Unding would indeed hit Naga City, while PAGASA   said it would miss Naga City.  Unfortunately for Naga City, Michael won   that bet.<br />
