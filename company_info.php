               <p><span class="newcontent">Our motto is:</span><img src="789.jpg" width="420" height="28" /><br />
                 <br />                 
                 <span class="headertitle"><br />
                   Company Profile </span><br />
                 <br />                 
                 <span class="headertitle"><br />
                   Applied Expert Systems and Software, Incorporated (APEXS, Inc.)</span><br />
                   <span class="contents"><br />
                  </span>               </p>
<p class="newcontent">The Company, APEXS, Inc. was formed to bring hi-tech state-of-the art products and services to the Philippine market. The company was incorporated in 1995 in Lapu-lapu City, Cebu, Philippines.</p>
<p class="newcontent">Mr. Shelly L. Shannon is the president, CEO and founder. The last thirteen years of his employment was as a senior staff employee with Saudi Aramco in Dhahran, Kingdom of Saudi Arabia the company from which he is an early retiree as of September 1994. After many years of overseas employment (including several years in the Philippines), in a number of countries, Mr. Shannon, (a U.S. citizen) and his wife Sophie (a native Cebuana) applied for and were granted a special retiree visa by the Philippine government as investors. In the Philippines, APEXS, Inc. is the exclusive distributor/manufacturer of the following products: </p>
<p class="newcontent"><br />
  1. <strong>DriveRight</strong> (Davis Instruments USA) - a microcomputer device that automatically monitors, records and sets limits to driver and vehicle performance. This type of microprocessor-based device is widely used by fleet owners, it is a cost effective tool in vehicle management control and as a safety device to monitor a driver's performance on the road.</p>
<p class="newcontent">2. <strong>CarChip</strong> (Davis  Instruments USA) - plugs  into the OBDII connector in your car, and continuously collects and stores data  from the car's computer control systems. CarChip Pro model is an all-in-one  engine performance and driving monitor. About the size of a key fob, it's a  great way to keep track of your vehicle, and your or others' driving.<br />
  <br />
  3. <strong>Vantage Pro2 Plus and Vantage Vue  Weather Station </strong>(Davis Instruments USA) &ndash; a state-of-the-art  technology at affordable prices. Used by thousands of schools, colleges, and  universities throughout the world, they're also used in agriculture, industry, research, and by homeowners, expeditions, storm chasers, and government  agencies. <br />
  &nbsp;&nbsp;&nbsp;&nbsp; Our newest wireless  weather station provides accurate, reliable weather monitoring in a  self-contained, easy-to-install system. Vantage Vue gives you all the weather  data you need for home use, education, boating and more. Sleek but tough&mdash;your  outdoor sensor suite sets up in minutes and starts sending data wirelessly to  your console anywhere in your home, school, or business.<br />
  <br />
  4. <strong>Teltonika  GPS Tracker </strong>(Lithuania)  &ndash; a state-of-the-art gps tracking system that provides user an ability to track vehicles and  assets location real time. Features such as Geofencing and real time fuel  monitoring are all in the product.</p>
<p class="newcontent">5
  . <strong>Parallax, Inc.</strong> (USA) - In 1993 the original BASIC Stamp was released. The attention of hobbyists and engineers was very evident as they purchased the device to replace larger, expensive control equipment and build projects they could have only dreamed about. The BASIC Stamp's ease of use and useful I/O command's made it easy to connect to motors, sensors, buttons, LCDs and other electronic components. <br />
  <br />
  6
  . <strong>APEXS V-Count II</strong> <strong>EL </strong>- Our low cost, second generation product is the ideal technology solution for helping newly licensed teenage drivers develop safe driving awareness with the family car. Help parents in monitoring and educating their teenage drivers. Enables vehicle fleet owners, managers and parents to Set, Monitor and Enforce maximum allowed vehicle speed to reduce accident and vehicle maintenance cost. The V-Count has an option that enables the engine to be shut off for 5 minutes when there is an over speed violation. </p>
<p class="newcontent">Our new model motor vehicle speed monitoring product (SpeedSpy) does not require any installation wiring. It is simply plugged into a special connector (DLC) located in the cabin of most vehicles manufactured since 1996.We are currently in the market testing stage now.</p>
<span class="newcontent">7
				. <strong>DriveRight Precision Pulse Reference</strong> (DPR) - A precision pulse generator specifically designed and manufactured by APEXS, Inc. for Davis Instruments, the manufacturer of DriveRight&reg;. The DPR generates precision pulses to simulate Vehicle Speed Signal (VSS) at 40 km/hr (25mph) at four (4) different selectable pulses per mile (PPM).<br />
                <br />
8
				. <strong>Timed Engine Shut-Off (TESO)</strong> -
				For owners of diesel powered and turbo aspirated vehicles, the APEXS TESO will protect and extend the engine life of their diesel and gasoline engines. TESO also monitors the engine coolant and oil pressure and will shut off the engine if a high temperature or low oil pressure condition is detected. To assist in vehicle maintenance scheduling, TESO maintains a permanent record of the engine run and idle times. As of 2005 it has been used in the delivery vehicles of the international courier DHL in the Philippines and received additional purchase orders in 2007. FedEx has also expressed interest in our product for its delivery vehicles. </span>
<p class="newcontent"><span class="newcontentItalic">Pool of Satisfied Customers.</span> APEXS, Inc. enjoys the confidence of both local and international corporations in terms of the DriveRight. Among the leading local transport and service companies that trust our product are: Filipinas Transport Corporation, Cebu Ace Trucking Corporation, Kimwa Construction &amp; Development Corporation, KCC Warehouse Plaza (General Santos City), Corominas Bus Liner, Gemini Trucking, ACG Transport (San Carlos City), Baysouth Transport Incorporated, Purolator Express Courier of Canada, Primary Structures Corporation and others. Likewise, Sedco Forex International Drilling Inc. for their operations in Singapore, Thailand, Malaysia, Japan, Vietnam, Brunei, and Australia belong to the pool of our satisfied customers using the DriveRight. </p>
<p class="newcontent">APEXS, Inc. is the official vendor/supplier of DriveRight to the Schlumberger Group of Oilfield Service Companies in Southeast Asia. <br />
  <br />
