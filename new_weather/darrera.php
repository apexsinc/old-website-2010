		<li span class="content">Resolution: same as Davis VP2</span></li>
        <li span class="content">Accuracy: ±2%</span></li>
        </ul>
        
 <p align="left" class="third">Wind direction</p>
   <ul>
		<li span class="content">Range: 0º to 360º</span></li>
		<li span class="content">Resolution: same as Davis VP2</span></li>
        <li span class="content">Accuracy: ±2º</span></li>
        </ul> 
        
 <h1><span class="content"><strong>Electrical Specifications</strong></span></h1> 
