                                      <p class="first">High Performance Technology                                      </p>
                                      <p class="third">The 3R UAV01 is a 2-axis ultrasonic anemometer that measures the wind speed and direction with high accuracy and reliability thanks to its advanced technology.                                      </p>
                                      <p align="left" class="third">It is especially designed to directly replace the Davis VP2 standard   wind cups anemometer without needing to use any other component or   transmitter apart from a power supply.</p>
                                      <p align="left" class="third">To determine the wind speed and direction, the sensor emits ultrasonic   pulses from one transducer to the opposite one along 2 orthogonal axis   and then measures the elapsed time between the emission and the   reception of the pulses. The measurements are done in both directions of   each path to avoid errors caused by the current environmental   conditions of temperature, humidity and barometric pressure.</p>
                                      <p align="left" class="third">Its high performance makes it ideal for all kind of security and control   applications in roads, buildings, ski resorts, theme parks, harbors,   etc. Its low power consumption allows to use it in installations powered   by solar panels.</p>

     <p class="first">Rugged and Weatherproof Design</p>
      <p align="left" class="third">The sensor features a very rugged design to ensure high resistance to weathering and has no moving parts that could fall off, break or freeze, which minimizes the need of periodic maintenance.</p>
       <p class="first">Technical specifications</p>
