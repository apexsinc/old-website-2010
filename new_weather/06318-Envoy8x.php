                                                          <p class="content">With 32 times the memory of our Weather Envoy, Envoy8X receives   and logs data simultaneously from up to eight transmitting stations, in   any combination--even eight Integrated Sensor Suites or eight Leaf   Wetness &amp; Soil Moisture/Temperature Stations! That means you can get   data from 32 soil moisture sensors on one Envoy8X! Includes robust   software that allows for data from many sensors and multiple Envoy8Xs to   produce very large databases in Microsoft Access&reg;, MySQL&reg;, or Microsoft   SQL Server&reg;. You select which sensors to log, and choose a logging   interval from 10 seconds to 2 hours. Use archive intervals as small as   30 seconds. You can even add select third-party sensors that report   readings in a 0-3 volt range. Data can be easily exported to a   spreadsheet program such as Excel&reg;, or to WeatherLink to take advantage   of WeatherLink's graphing and reports.</p>
                                                          <h2 class="content">The Envoy8X includes: </h2>
                                                          <ul>
                                                            <li class="content">Envoy8X</li>
                                                            <li class="content">Envoy8X software</li>
                                                            <li class="content">AC adapter</li>
                                                          </ul>
                                                          <p class="content">Also works with AA batteries (not included). Requires at least one   Davis sensor and a WeatherLink USB (#6510USB) or Serial (#6510SER) data   logger, sold separately.</p>
                                                          <h2 class="content">Key features:</h2>
                                                          <ul>
                                                            <li class="content">Collect and store weather data in your own database from up to 8 Davis stations or 32 soil moisture sensors.</li>
                                                            <li class="content">Data can be exported to and used in Microsoft Access, Excel, SQL Server.</li>
                                                            <li class="content">32 times more data logging capacity than our regular Envoy.</li>
                                                            <li class="content">Use archive intervals as small as 30 seconds.</li>
                                                            <li class="content">Export for use with WeatherLink</li>
                                                            <li class="content">Fully compatible with our wireless Vantage Pro2 and Vantage Vue stations and sensors. This includes repeaters.</li>
                                                            <li class="content">Compatible with a solar power kit (#6610) and System Shelter (#7724)</li>
                                                            <li class="content">Use non-Davis sensors through the &quot;raw data&quot; specification</li>
                                                          </ul>
                                                          <p class="content">Storage capacity depends upon the logging interval, but is so large   that you can download a remote field installation when it's most   convenient. For example, an Envoy8X logging two Integrated Sensor Suites   and two Soil Moisture/Leaf Wetness stations at a 15-minute update   interval will store over 365 days worth of data.</p>
                                                          <p class="content">Envoy8X has built-in temperature and humidity sensors, as well as a   barometer. The software will report data from those sensors as &quot;inside&quot;   data, effectively giving you a ninth &quot;station.&quot; You may connect an   external temperature probe (6475 or 6477) to replace the Envoy8X's   internal temperature probe. Receives data from all Vantage Pro2 radios   and repeaters. Includes an AC-power adapter; may also be run using Solar   Power Kit (7707), or on three AA batteries. Expected battery life is   one to four months depending on the logging interval. Requires   WeatherLink for Windows (USB or Serial).</p>
