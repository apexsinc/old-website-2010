                                    <p>The compact console is loaded with features, including a backlit LCD screen and glow-in-the-dark keypad that makes viewing weather data easy, day or night. Unique Weather Center feature allows you to view multiple screens of weather data simply and quickly.</p>
                                    </span>
                                      <p class="content">Vantage Vue gives you the weather data you need. You'll see current  weather conditions, including indoor and outdoor temperature and  humidity, barometric pressure, wind speed and direction, dew point and  rainfall. You'll also get weather forecast icons, moon phase,  sunrise/sunset times, graphing of weather trends, alarms and more.  Vantage Vue also boasts the unique Weather Center button that displays  additional data on weather variables for today and over the last 25  days.
                                      <ul>
                                        <li class="content"><strong>TIME &amp; DATE </strong><br>
                                      View the current time and date or time of sunrise/sunset.</li>
                                        <li class="content"><strong>MOON PHASE</strong><br>
                                      See the eight phases of the moon from new to full.</li>
                                        <li class="content"><strong>FORECAST ICONS</strong><br>
                                      Icons let you know to expect sun, part sun, clouds, rain, or snow.</li>
                                        <li class="content"><strong>TEMPERATURE &amp; HUMIDITY</strong><br>
                                      Console updates outside temperature every 10 seconds and inside  temperature every minute. In addition, outside humidity Updates every  50 seconds and inside humidity every minute.</li>
                                        <li class="content"><strong>RETRANSMITS</strong><br>
                                        Main console can extend range to additional consoles up to 1,000 ft. (300 m).</li>
                                        <li class="content"><strong>BAROMETER</strong><br>
                                        Five-position trend arrow shows whether barometric pressure is rising, falling, or stable.<br>
                                        <li><span class="content"><strong>VARIABLE DISPLAY OPTIONS </strong><br>
                                      See updated variables every 10 seconds for heat index and wind chill to get a better "feel" for the outside environment.</span><br><li class="content">
                                      <strong>RAIN</strong><br>
                                      Console updates rain totals and rain rate every 20 seconds for the last 25 hours, days, months.<li class="content"><strong>"CHANGE IN WEATHER SINCE YESTERDAY" FEATURE</strong><br>
                                      Allows comparisons of changes of temperature and barometric pressure from day to day.<br><li class="content"><strong>WEATHER CENTER</strong><br>
                                  Weather Center provides additional information for each weather  variable, such as daily highs and lows, temperature changes by the hour  and barometric value changes. Also displays astronomical data such as  meteor showers.<br><li><span class="content"> <strong>USER-FRIENDLY KEYPAD</strong><br>
                                  Domed buttons are easy to sense and press. Glow-in-the-dark keypad  buttons give you access to weather information all day and all night.  The Done key returns user to "current" screen from anywhere in the  program.<br>
                                  </span>
                                  <li class="content"><strong>GRAPHS</strong><br>
                                      Graph the last 25 hours, days or months. View up to 50 graphs including  temperature, rain, rain rate, wind and barometric pressure&mdash;all on the  console.<br><li><span class="content"><strong>"THIS TIME YESTER DAY" FEATURE</strong><br>
                                      Data point on the graph indicates the weather during the same time  yesterday to help compare and analyze day-to-day weather trends.<br>
                                      </span>
                                      <li><span class="content"><strong>ALARMS</strong><br>
                                      Set 22 simultaneous, different alarms to warn of dangers such as high  winds, freezing temperatures, rain rate, flood warning, and more.<br>
                                      </span>
                                      <li class="content"><strong>WIND SPEED</strong><br>
                                      See windspeed updates every 2.5 seconds. Displays windspeed in miles  per hour, meters per second, kilometers per hour and knots. Provides  the average and high wind speed for two-minute and ten-minute intervals.</li></ul>
