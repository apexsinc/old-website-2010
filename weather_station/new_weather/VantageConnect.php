                                      <p class="first">Vantage Connect® for Wireless Stations (Select Countries)</p>
                                      <p class="second">Real-time weather update delivered right to your smartphone or computer.</p>
                                      <p class="third"><strong>Remote weather data, versatile integration.</strong> Cellular-based, solar-powered unit sends remote weather station data to the Internet.<Br /><br />Vantage Connect sends comprehensive weather data to the web from remote locations, such as vacation or rental homes, ranches, orchards, farms, vineyards and fire hazard areas that lack power or a person to monitor the weather.</p>
                                      <p class="second">What is Vantage Connect?</p>
<ul>
<li span class="content">A weather station receiver and cellular modem.</li>
<li span class="content">Solar powered with battery backup; does not require any external power sources.</li>
<li span class="content">Self-contained in a weather-resistant shelter.</li></ul>

 <p class="second">How does Vantage Connect work?</p>
 <ul>
<li span class="content">Uploads weather data through a cellular connection to WeatherLink.com in 5, 15, or 60-minute intervals, depending on the service plan you choose.</li>
<li span class="content">Alerts you to alarm conditions in the field the moment they occur.</li>
<li span class="content">Pairs with Davis' Vantage Pro2 or Vantage Vue integrated sensor suite.</li></ul>

<p class="first">Vantage Connect Includes</p>
<ul>
<li span class="content">Vantage Connect receiver and cellular modem</li>
<li span class="content">Heavy-duty battery backup </li>
<li span class="content">WeatherLink software</li>
<li span class="content">Your own WeatherLink Network account on <a href="http://www.davisnet.com/weather/products/vantage-connect/service-plans.asp?pNum=06620OV&cISO=none#plans" target="_blank">WeatherLink.com</a> with purchase of a Vantage Connect service plan</li>
<li span class="content">Hardware to mount to a vertical pole</li></ul>

<p class="first">Compatibility & System Requirements</p>
<p class="third"><strong>IMPORTANT:</strong> There are different versions of Vantage Connect that operate in specific countries. Please be sure to choose the product and service plan that is right for your country.</p>
<ul class="c">
<li span class="content">The wireless Vantage Connect integrates with:</li>
<ul class="a">
<li span class="content">Any existing Vantage Pro2 or Vantage Vue weather station or Weather Envoy</li>
<li span class="content">Up to eight wireless Davis Weather Stations, including:</li>
<ul class="b">
<li span class="content">1 Vantage Pro2 or Vantage Vue ISS</li>
<li span class="content">1 Wireless Leaf & Soil Moisture/Temperature Station </li>
<li span class="content">2 Wireless Temperature/Humidity Stations </li>
<li span class="content">3 Wireless Temperature Stations</li>
<li span class="content">1 Anemometer/Sensor Transmitter Kit</li>
</ul></ul>
<li span class="content">Requires at least one Davis Integrated Sensor Suite (ISS) or special purpose station.</li>
<li span class="content">A console or Envoy is not required.</li>
<li span class="content">WeatherLink software requires a computer running Windows® XP or above.</li>
<li span class="content">iPhone®, Android™ and tablet compatible.</li>
<li span class="content">Not compatible with original Vantage Pro, Envoy8X, or Mac Software.</li></ul>
<p class="first">Accessories</p>
<p class="third">In areas with prolonged periods of low light and accessible power, add the 6710 Optional AC Charger Kit.<br /><br />
Replace Vantage Connect's internal temperature probe with an external temperature probe (6475 or 6477).</p>
